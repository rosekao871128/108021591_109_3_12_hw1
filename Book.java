public class Book {
    
    private String name , author , page, price, collect;

    public void setName(String setname){
        this.name = setname;
    }

    public String getName(){
        return name;
    }

    public void setAuthor(String setauthor){
        this.author = setauthor;
    }

    public String getAuthor(){
        return author;
    }

    public void setPage(String setpage){
        this.page = setpage;
    }

    public String getPage(){
        return page;
    }

    public void setPrice(String setprice){
        this.price = setprice;
    }

    public String getPrice(){
        return price;
    }

    public void setCollect(String setcollect){
        this.collect = setcollect;
    }

    public String getCollect(){
        return collect;
    }
}